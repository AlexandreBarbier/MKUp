import UIKit

extension UIColor {
    
    class func MKGreyColor() -> UIColor! {
        return UIColor(red: 102.0/255.0, green: 116.0/255.0, blue: 123.0/255.0, alpha: 1.0)
    }
    
    class func MKWhiteColor() -> UIColor! {
        return UIColor(red: 255.0/255.0, green: 255.0/255.0, blue: 248.0/255.0, alpha: 1.0)
    }
    
    class func MKGreenColor() -> UIColor! {
        return UIColor(red: 186.0/255.0, green: 233.0/255.0, blue: 206.0/255.0, alpha: 1.0)
    }
    
    class func MKBrownColor() -> UIColor! {
        return UIColor(red: 55.0/255.0, green: 30.0/255.0, blue: 12.0/255.0, alpha: 1.0)
    }
    
    class func MKDarkBrownColor() -> UIColor! {
        return UIColor(red: 37.0/255.0, green: 18.0/255.0, blue: 2.0/255.0, alpha: 1.0)
    }
}